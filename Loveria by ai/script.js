// ── APPLY LANGUAGE ─────────────────────────────────────────────
function applyLang(lang) {
  const t = translations[lang];
  document.title = t.pageTitle;

  // Photo descriptions
  document.querySelectorAll('.photo-desc').forEach(el => {
      if (el.dataset.eng && el.dataset.ben) {
          el.textContent = lang === 'eng' ? el.dataset.eng : el.dataset.ben;
      }
  });

  // Hero
  const heroTitle = document.querySelector('.hero-text h1');
  if (heroTitle) heroTitle.innerHTML = t.heroTitle;
  const eyebrow = document.querySelector('.hero-eyebrow');
  if (eyebrow) eyebrow.textContent = t.heroEyebrow;
  const heroSub = document.querySelector('.hero-sub');
  if (heroSub) heroSub.textContent = t.heroSub;

  // Note
  const note = document.querySelector('.note');
  if (note) note.textContent = t.note;

  // Details title
  const detailsTitle = document.querySelector('.details-section .section-title');
  if (detailsTitle) detailsTitle.textContent = t.detailsTitle;

  // Cards
  const cardLines = document.querySelectorAll('.card-line');
  const cardNames = document.querySelectorAll('.card-name');
  if (cardNames[0]) cardNames[0].textContent = t.souravName;
  if (cardNames[1]) cardNames[1].textContent = t.parbotiName;
  const labels = [t.bornLabel, t.homeLabel, t.lovesLabel, t.instaLabel];
  const souravVals = [t.souravBorn, t.souravHome, t.souravLoves];
  const parbotiVals = [t.parbotiBorn, t.parbotiHome, t.parbotiLoves];

  // Sourav: lines 0,1,2 (line 3 is Instagram link — keep as-is)
  [0,1,2].forEach(i => {
    if (cardLines[i]) cardLines[i].innerHTML = `<span>${labels[i]}</span> ${souravVals[i]}`;
  });
  if (cardLines[3]) {
    const link = cardLines[3].querySelector('a');
    const linkHTML = link ? link.outerHTML : '';
    cardLines[3].innerHTML = `<span>${t.instaLabel}</span>${linkHTML}`;
  }

  // Parboti: lines 4,5,6 (line 7 is Instagram link)
  [0,1,2].forEach(i => {
    if (cardLines[i+4]) cardLines[i+4].innerHTML = `<span>${labels[i]}</span> ${parbotiVals[i]}`;
  });
  if (cardLines[7]) {
    const link = cardLines[7].querySelector('a');
    const linkHTML = link ? link.outerHTML : '';
    cardLines[7].innerHTML = `<span>${t.instaLabel}</span>${linkHTML}`;
  }

  // Gallery title
  const galleryTitle = document.querySelector('.gallery-section .section-title');
  if (galleryTitle) galleryTitle.textContent = t.galleryTitle;

  // Photo descriptions
  document.querySelectorAll('.photo-desc').forEach(el => {
    if (el.dataset.eng && el.dataset.ben) {
        el.textContent = lang === 'eng' ? el.dataset.eng : el.dataset.ben;
    } else {
        el.textContent = t.photoDesc;
    }
});

  // Timeline title
  const tlTitle = document.querySelector('.timeline-section .section-title');
  if (tlTitle) tlTitle.textContent = t.timelineTitle;

  // Timeline items
  const tlItems = document.querySelectorAll('.tl-item');
  tlItems.forEach((item, i) => {
    if (!t.tl[i]) return;
    const dateEl = item.querySelector('.tl-date');
    const textEl = item.querySelector('.tl-text');
    if (dateEl) dateEl.textContent = t.tl[i].date;
    if (textEl) textEl.innerHTML = t.tl[i].text;
  });

  // Footer
  const footer = document.querySelector('footer p');
  if (footer) footer.textContent = t.footer;
}

// ── TOGGLE LOGIC ───────────────────────────────────────────────
window.addEventListener('DOMContentLoaded', () => {

    // Preload heart image
    const heartImg = new Image();
    heartImg.src = 'recources/Pink_Heart_Decorative_Clipart-122479836.png';

    // Floating hearts
    function createHeart() {
        const rotate = Math.random() > 0.5 ? 45 : -45;
        const heart = heartImg.cloneNode();
        const size = Math.random() * 80 + 40;
        heart.style.cssText = `
            position: fixed;
            bottom: -60px;
            left: ${Math.random() * 100}vw;
            width: ${size}px;
            height: ${size}px;
            object-fit: contain;
            opacity: ${Math.random() * 0.2 + 0.3};
            animation: floatUp${rotate < 0 ? 'Anti' : ''} ${Math.random() * 3 + 6}s linear forwards;
            pointer-events: none;
        z    -index: 9999;
        `;
        document.body.appendChild(heart);
        setTimeout(() => heart.remove(), 15000);
    }

    // Start hearts on load
    for (let i = 0; i < 15; i++) {
        setTimeout(createHeart, i * 300);
    }
  // Hero entrance animation
  const hero = document.querySelector('.hero-text');
  if (hero) {
    hero.style.opacity = '0';
    hero.style.transform = 'translateY(20px)';
    hero.style.transition = 'opacity 1.4s ease, transform 1.4s ease';
    requestAnimationFrame(() => {
      setTimeout(() => {
        hero.style.opacity = '1';
        hero.style.transform = 'translateY(0)';
      }, 200);
    });
  }

  // Language toggle
  const toggle = document.getElementById('langToggle');
  if (toggle) {
    toggle.addEventListener('change', () => {
      applyLang(toggle.checked ? 'ben' : 'eng');
    });
  }

  // Empty frame click hint
  document.querySelectorAll('.empty-frame').forEach(frame => {
    frame.addEventListener('click', () => {
      alert('ছবি যোগ করতে: index.html এ এই empty-frame div টা একটা <img> tag দিয়ে replace করো। 💕');
    });
  });
});